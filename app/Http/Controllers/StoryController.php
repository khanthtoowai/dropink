     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = auth()->user()->following()->pluck('profiles.user_id');
        $stories = Story::whereIn('user_id', $users)->latest()->get();
        return view('profiles.index', [
            'stories' => $stories
